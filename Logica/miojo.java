public class miojo {
    public static void main(String[] args) {

        // Início do algoritmo
        System.out.println("INÍCIO");

        // ================================
        // PREPARAR UTENSÍLIOS E INGREDIENTES
        // ================================
        System.out.println("Pegar uma panela");                 // Utensílio para ferver a água
        System.out.println("Ligar a torneira");                 // Inicia o fluxo de água
        System.out.println("Encher a panela com água");         // Coloca a quantidade necessária para o miojo
        System.out.println("Desligar a torneira");              // Evita desperdício de água

        // ================================
        // AQUECER A ÁGUA
        // ================================
        System.out.println("Ligar o fogo do fogão");            // Começa a aquecer a panela
        System.out.println("Colocar a panela sobre o fogo");    // Posiciona a panela corretamente
        System.out.println("Aguardar até que a água ferva");    // Espera a água atingir ebulição

        // ================================
        // COZINHAR O MIOJO
        // ================================
        System.out.println("Abrir o pacote de miojo");          // Abre a embalagem
        System.out.println("Retirar o tempero");                // Separa o tempero do macarrão
        System.out.println("Colocar o miojo na água fervendo"); // Inicia o cozimento
        System.out.println("Adicionar o tempero");              // Tempera o miojo
        System.out.println("Aguardar 3 minutos");               // Tempo padrão para cozinhar

        // ================================
        // FINALIZAR E SERVIR
        // ================================
        System.out.println("Desligar o fogo");                  // Finaliza o cozimento
        System.out.println("Retirar a panela com cuidado");     // Evita queimaduras
        System.out.println("Pegar um prato e um garfo");        // Utensílios para servir
        System.out.println("Servir o miojo no prato");          // Coloca o miojo para comer
        System.out.println("Comer o miojo");                    // Último passo: aproveitar!

        // Fim do algoritmo
        System.out.println("FIM");
    }
}

