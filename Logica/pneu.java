public class pneu {

    public static void main(String[] args) {

        // Início do algoritmo
        System.out.println("INÍCIO");

        // ================================
        // SINALIZAR E PREPARAR O LOCAL
        // ================================
        System.out.println("Ligar o pisca-alerta");                           // Sinaliza que o carro está parado
        System.out.println("Colocar o triângulo de sinalização");             // Garante segurança do local
        System.out.println("Abrir o porta-malas");                            // Acessa as ferramentas necessárias
        System.out.println("Pegar o macaco, a chave de roda e o estepe");     // Ferramentas essenciais para troca

        // ================================
        // LEVANTAR O VEÍCULO
        // ================================
        System.out.println("Posicionar o macaco no local indicado do carro"); // Evita danos ao veículo
        System.out.println("Erguer o carro até que o pneu danificado fique suspenso"); 
        // Permite remover o pneu sem atrito no chão

        // ================================
        // REMOVER O PNEU DANIFICADO
        // ================================
        System.out.println("Afrouxar e remover os parafusos");                // Libera o pneu
        System.out.println("Retirar o pneu furado");                          // Remove o pneu danificado

        // ================================
        // INSTALAR O PNEU NOVO (ESTEPE)
        // ================================
        System.out.println("Colocar o estepe no lugar");                      // Substitui pelo pneu reserva
        System.out.println("Recolocar e apertar os parafusos com a chave de roda"); 
        // Garante firmeza do pneu
        System.out.println("Verificar se o pneu está bem fixado");            // Conferência importante

        // ================================
        // FINALIZAR PROCESSO
        // ================================
        System.out.println("Abaixar o carro com o macaco");                   // Retorna o carro ao solo
        System.out.println("Retirar o macaco debaixo do carro");              // Evita danos e riscos
        System.out.println("Guardar todas as ferramentas e o pneu danificado no porta-malas"); 
        // Organização pós-troca
        System.out.println("Fechar o porta-malas");                           // Finaliza o uso do porta-malas
        System.out.println("Seguir viagem com segurança");                    // Processo concluído

        // Fim do algoritmo
        System.out.println("FIM");
    }
}


