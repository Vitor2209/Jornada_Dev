package java.cafe;
public class cafe {

    public static void main(String[] args) {

        // Marca o início do algoritmo
        System.out.println("INÍCIO");

        // ================================
        // PREPARAR UTENSÍLIOS E INGREDIENTES
        // ================================
        System.out.println("Pegar uma xícara");                // Utensílio para beber o café
        System.out.println("Pegar uma colher");                // Para misturar açúcar ou adoçante
        System.out.println("Pegar um filtro de papel");        // Usado no coador
        System.out.println("Pegar o coador de café");          // Onde o pó de café será colocado
        System.out.println("Pegar o pó de café");              // Ingrediente principal
        System.out.println("Pegar a garrafa térmica ou bule"); // Onde o café será coado
        System.out.println("Pegar água limpa");                // Para aquecer e fazer o café

        // ================================
        // AQUECER A ÁGUA
        // ================================
        System.out.println("Colocar a água em uma chaleira");                  // Coloca a água no recipiente
        System.out.println("Levar a chaleira ao fogo ou ligar o fervedor");    // Inicia o aquecimento
        System.out.println("Esperar até a água começar a ferver");             // Aguarda o ponto de ebulição
        System.out.println("Desligar o fogo ou o fervedor");                   // Evita superaquecer a água

        // ================================
        // FAZER O CAFÉ
        // ================================
        System.out.println("Colocar o filtro de papel no coador");             // Prepara o coador
        System.out.println("Adicionar pó de café ao filtro");                  // Coloca a quantidade de café desejada
        System.out.println("Posicionar o coador sobre a garrafa ou bule");     // Deixa tudo alinhado
        System.out.println("Despejar lentamente a água quente sobre o pó");    // Processo de coar
        System.out.println("Aguardar toda a água passar pelo filtro");         // Espera filtrar completamente
        System.out.println("Retirar o coador e descartar o filtro usado");     // Finaliza a filtragem

        // ================================
        // SERVIR O CAFÉ
        // ================================
        System.out.println("Despejar o café pronto na xícara");                // Coloca o café para servir
        System.out.println("Adicionar açúcar ou adoçante, se desejar");        // Ajusta o sabor
        System.out.println("Mexer com a colher");                              // Mistura tudo
        System.out.println("Servir e aproveitar o café");                      // Toma o café :)

        // Marca o fim do algoritmo
        System.out.println("FIM");
    }
}